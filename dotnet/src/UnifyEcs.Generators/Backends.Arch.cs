        }

        private static string GetRefPrefix(RefKind refKind) => refKind switch
        {
            RefKind.Ref => "ref ",
            RefKind.In => "in ",
            RefKind.Out => "out ",
            _ => string.Empty
        };

        private static bool CanUseInlineQuery(SystemModel system, QueryModel query)
        {
            // Only support void-returning methods
            if (!string.Equals(query.ReturnTypeName, "void", StringComparison.Ordinal) &&
                !string.Equals(query.ReturnTypeName, "System.Void", StringComparison.Ordinal))
            {
                return false;
            }

            if (query.ParameterTypeNames.Length == 0)
            {
                return false;
            }

            for (var i = 0; i < query.ParameterTypeNames.Length; i++)
            {
                var typeName = query.ParameterTypeNames[i];

                // For InlineQuery v1 we don't support entity parameters; those would
                // require IForEachWithEntity instead of IForEach.
                if (string.Equals(typeName, "Entity", StringComparison.Ordinal) ||
                    string.Equals(typeName, "UnifyECS.Entity", StringComparison.Ordinal) ||
                    string.Equals(typeName, "Arch.Core.Entity", StringComparison.Ordinal) ||
                    typeName.EndsWith(".Entity", StringComparison.Ordinal))
                {
                    return false;
                }

                // Require ref parameters so that they match IForEach.Update signature.
                if (query.ParameterRefKinds[i] != RefKind.Ref)
                {
                    return false;
                }

                // Avoid nullable/optional component types in v1.
                if (typeName.Contains("?", StringComparison.Ordinal) ||
                    typeName.StartsWith("System.Nullable<", StringComparison.Ordinal))
                {
                    return false;
                }
            }

            return true;
        }

        private static void AppendQueryDescription(StringBuilder sb, QueryModel query)
        {
            if (query.AllComponents.Length > 0)
            {
                sb.Append(".WithAll<")
                  .Append(string.Join(", ", query.AllComponents))
                  .Append(">()");
            }

            if (query.AnyComponents.Length > 0)
            {
                sb.Append(".WithAny<")
                  .Append(string.Join(", ", query.AnyComponents))
                  .Append(">()");
            }

            if (query.NoneComponents.Length > 0)
            {
                sb.Append(".WithNone<")
                  .Append(string.Join(", ", query.NoneComponents))
                  .Append(">()");
            }

            if (query.ExclusiveComponents.Length > 0)
            {
                sb.Append(".WithExclusive<")
                  .Append(string.Join(", ", query.ExclusiveComponents))
                  .Append(">()");
            }
        }

        private static string ToAccessibilityString(Accessibility accessibility) => accessibility switch
        {
            Accessibility.Public => "public",
            Accessibility.Internal => "internal",
            Accessibility.Protected => "protected",
            Accessibility.Private => "private",
            Accessibility.ProtectedOrInternal => "protected internal",
            Accessibility.ProtectedAndInternal => "private protected",
            _ => "internal"
        };

        private static string MakeSafeIdentifier(string fullName)
        {
            var sb = new StringBuilder(fullName.Length + 8);
            foreach (var ch in fullName)
            {
                if (char.IsLetterOrDigit(ch) || ch == '_')
                {
                    sb.Append(ch);
                }
                else
                {
                    sb.Append('_');
                }
            }

            if (sb.Length == 0)
            {
                return "_Reactive";
            }

            if (!char.IsLetter(sb[0]) && sb[0] != '_')
            {
                sb.Insert(0, '_');
            }

            return sb.ToString();
        }
