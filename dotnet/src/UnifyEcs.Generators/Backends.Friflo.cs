        public string? EmitReactiveEmulation(SystemModel system)
        {
            if (system.ReactiveHandlers is null || system.ReactiveHandlers.Length == 0)
                return null;

            var groups = system.ReactiveHandlers
                .GroupBy(h => h.ComponentTypeName)
                .ToArray();

            if (groups.Length == 0)
                return null;

            var sb = new StringBuilder();

            sb.AppendLine("// <auto-generated/>");
            sb.AppendLine("// UnifyECS Friflo reactive emulation (OnAdded/OnChanged/OnRemoved)");
            sb.AppendLine("using System;");
            sb.AppendLine("using System.Collections.Generic;");
            sb.AppendLine("using Friflo.Engine.ECS;");
            sb.AppendLine("using UnifyECS;");
            sb.AppendLine();

            if (!string.IsNullOrEmpty(system.Namespace))
            {
                sb.Append("namespace ").Append(system.Namespace).AppendLine();
                sb.AppendLine("{");
            }

            var indent = string.IsNullOrEmpty(system.Namespace) ? string.Empty : "    ";

            sb.Append(indent)
              .Append("partial class ")
              .Append(system.Name)
              .AppendLine();
            sb.Append(indent).AppendLine("{");

            foreach (var group in groups)
            {
                var componentTypeName = group.Key;
                var safeName = MakeSafeIdentifier(componentTypeName);

                var handlers = group.ToArray();
                var onAdded = handlers.Where(h => h.Kind == ReactiveKind.OnAdded).ToArray();
                var onChanged = handlers.Where(h => h.Kind == ReactiveKind.OnChanged).ToArray();
                var onRemoved = handlers.Where(h => h.Kind == ReactiveKind.OnRemoved).ToArray();

                // Snapshot dictionary for previous frame values
                sb.Append(indent).Append("    private readonly Dictionary<global::UnifyECS.Entity, ")
                  .Append(componentTypeName)
                  .Append("> __reactivePrev_")
                  .Append(safeName)
                  .Append(" = new Dictionary<global::UnifyECS.Entity, ")
                  .Append(componentTypeName)
                  .AppendLine(">();");

                // Helper: __RunReactive_<T>(EntityStore entities)
                sb.Append(indent).Append("    private void __RunReactive_")
                  .Append(safeName)
                  .AppendLine("(EntityStore entities)");
                sb.Append(indent).AppendLine("    {");

                sb.Append(indent).AppendLine("        var current = new Dictionary<global::UnifyECS.Entity, " + componentTypeName + ">();");
                sb.AppendLine();

                sb.Append(indent).AppendLine("        // Build current snapshot and fire OnAdded/OnChanged");
                sb.Append(indent).AppendLine("        var query = entities.Query().WithAll<" + componentTypeName + ">();");
                sb.Append(indent).AppendLine("        foreach (var (componentSpan, entitySpan) in query.Chunks)");
                sb.Append(indent).AppendLine("        {");
                sb.Append(indent).AppendLine("            for (int i = 0; i < entitySpan.Length; i++)");
                sb.Append(indent).AppendLine("            {");
                sb.Append(indent).AppendLine("                var unifyEntity = FrifloWorld.MapEntity(entitySpan[i]);");
                sb.Append(indent).AppendLine("                var component = componentSpan[i];");
                sb.AppendLine();

                sb.Append(indent).AppendLine("            if (__reactivePrev_" + safeName + ".TryGetValue(unifyEntity, out var prev))");
                sb.Append(indent).AppendLine("            {");
                if (onChanged.Length > 0)
                {
                    sb.Append(indent).AppendLine("                if (!EqualityComparer<" + componentTypeName + ">.Default.Equals(prev, component))");
                    sb.Append(indent).AppendLine("                {");
                    foreach (var handler in onChanged)
                    {
                        sb.Append(indent).Append("                    this.")
                          .Append(handler.MethodName)
                          .AppendLine("(unifyEntity, in component);");
                    }
                    sb.Append(indent).AppendLine("                }");
                }
                sb.Append(indent).AppendLine("            }");
                sb.Append(indent).AppendLine("            else");
                sb.Append(indent).AppendLine("            {");
                if (onAdded.Length > 0)
                {
                    foreach (var handler in onAdded)
                    {
                        sb.Append(indent).Append("                this.")
                          .Append(handler.MethodName)
                          .AppendLine("(unifyEntity, in component);");
                    }
                }
                sb.Append(indent).AppendLine("            }");
                sb.AppendLine();

                sb.Append(indent).AppendLine("            current[unifyEntity] = component;");
                sb.Append(indent).AppendLine("            }");
                sb.Append(indent).AppendLine("        }");
                sb.AppendLine();

                sb.Append(indent).AppendLine("        // Fire OnRemoved for entities that disappeared");
                sb.Append(indent).AppendLine("        foreach (var kv in __reactivePrev_" + safeName + ")");
                sb.Append(indent).AppendLine("        {");
                sb.Append(indent).AppendLine("            var entity = kv.Key;");
                sb.Append(indent).AppendLine("            var prev = kv.Value;");
                sb.AppendLine();
                sb.Append(indent).AppendLine("            if (!current.ContainsKey(entity))");
                sb.Append(indent).AppendLine("            {");
                if (onRemoved.Length > 0)
                {
                    foreach (var handler in onRemoved)
                    {
                        sb.Append(indent).Append("                this.")
                          .Append(handler.MethodName)
                          .AppendLine("(entity, in prev);");
                    }
                }
                sb.Append(indent).AppendLine("            }");
                sb.Append(indent).AppendLine("        }");
                sb.AppendLine();

                sb.Append(indent).AppendLine("        __reactivePrev_" + safeName + ".Clear();");
                sb.Append(indent).AppendLine("        foreach (var kv in current)");
                sb.Append(indent).AppendLine("        {");
                sb.Append(indent).AppendLine("            __reactivePrev_" + safeName + ".Add(kv.Key, kv.Value);");
                sb.Append(indent).AppendLine("        }");
                sb.Append(indent).AppendLine("    }");
                sb.AppendLine();
            }

            sb.Append(indent).AppendLine("}");

            if (!string.IsNullOrEmpty(system.Namespace))
            {
                sb.AppendLine("}");
            }

            return sb.ToString();
        }

        public string EmitBootstrap(IReadOnlyList<SystemModel> systems)
        {
            if (systems is null || systems.Count == 0)
            {
                return string.Empty;
            }

            var orderedSystems = systems
                .Where(static s => s.IsPartial)
                .OrderBy(static s => (int)s.Phase)
                .ThenBy(static s => s.GroupFullName ?? string.Empty, StringComparer.Ordinal)
                .ThenBy(static s => s.Order)
                .ThenBy(static s => s.FullName, StringComparer.Ordinal)
                .ToArray();

            if (orderedSystems.Length == 0)
            {
                return string.Empty;
            }

            var sb = new StringBuilder();
            sb.AppendLine("// <auto-generated/>");
            sb.AppendLine("#nullable enable");
            sb.AppendLine("using System;");
            sb.AppendLine();
            sb.AppendLine("namespace UnifyECS");
            sb.AppendLine("{");
            sb.AppendLine("    public static class GeneratedFrifloBootstrap");
            sb.AppendLine("    {");
            sb.AppendLine("        public static void RegisterAll(global::UnifyECS.FrifloSystemRunner runner)");
            sb.AppendLine("        {");
            sb.AppendLine("            RegisterAll(runner, services: null);");
            sb.AppendLine("        }");
            sb.AppendLine();
            sb.AppendLine("        public static void RegisterAll(global::UnifyECS.FrifloSystemRunner runner, global::System.IServiceProvider? services)");
            sb.AppendLine("        {");
            sb.AppendLine("            if (runner is null) throw new global::System.ArgumentNullException(nameof(runner));");
            sb.AppendLine("            var factory = CreateDefaultFactory(services);");
            sb.AppendLine("            RegisterAll(runner, factory);");
            sb.AppendLine("        }");
            sb.AppendLine();
            sb.AppendLine("        public static void RegisterAll(");
            sb.AppendLine("            global::UnifyECS.FrifloSystemRunner runner,");
            sb.AppendLine("            global::System.Func<global::System.Type, global::UnifyECS.IUnifySystem> factory)");
            sb.AppendLine("        {");
            sb.AppendLine("            if (runner is null) throw new global::System.ArgumentNullException(nameof(runner));");
            sb.AppendLine("            if (factory is null) throw new global::System.ArgumentNullException(nameof(factory));");
            sb.AppendLine();

            foreach (var system in orderedSystems)
            {
                sb.Append("            runner.Register((global::UnifyECS.IUnifySystem)factory(typeof(global::")
                  .Append(system.FullName)
                  .AppendLine("))); ");
            }

            sb.AppendLine("        }");
            sb.AppendLine();
            sb.AppendLine("        private static global::System.Func<global::System.Type, global::UnifyECS.IUnifySystem> CreateDefaultFactory(global::System.IServiceProvider? services)");
            sb.AppendLine("        {");
            sb.AppendLine("            if (services is null)");
            sb.AppendLine("            {");
            sb.AppendLine("                return static type => (global::UnifyECS.IUnifySystem)global::System.Activator.CreateInstance(type)!;");
            sb.AppendLine("            }");
            sb.AppendLine();
            sb.AppendLine("            return type => (global::UnifyECS.IUnifySystem)(services.GetService(type) ?? global::System.Activator.CreateInstance(type)!)!;");
            sb.AppendLine("        }");
            sb.AppendLine("    }");
            sb.AppendLine("}");

            return sb.ToString();
        }

        public string EmitNoOpStub(SystemModel system)
        {
            if (!system.IsPartial)
                return string.Empty;

            var sb = new StringBuilder();
            sb.AppendLine("// <auto-generated/>");
            sb.AppendLine("// UnifyECS Friflo backend no-op stub (missing features)");
            sb.AppendLine("using System;");
            sb.AppendLine("using Friflo.Engine.ECS;");
            sb.AppendLine();

            if (!string.IsNullOrEmpty(system.Namespace))
            {
                sb.Append("namespace ").Append(system.Namespace).AppendLine();
                sb.AppendLine("{");
            }

            var indent = string.IsNullOrEmpty(system.Namespace) ? string.Empty : "    ";
            var accessibility = ToAccessibilityString(system.Accessibility);

            sb.Append(indent)
              .Append(accessibility)
              .Append(" partial class ")
              .Append(system.Name)
              .Append(" : global::UnifyECS.IFrifloSystem")
              .AppendLine();
            sb.Append(indent).AppendLine("{");

            sb.Append(indent).AppendLine("    public void Execute(EntityStore entities, float deltaTime)");
            sb.Append(indent).AppendLine("    {");
            sb.Append(indent).AppendLine("        // No-op: required features are not available on Friflo backend.");
            sb.Append(indent).AppendLine("    }");

            sb.Append(indent).AppendLine("}");

            if (!string.IsNullOrEmpty(system.Namespace))
            {
                sb.AppendLine("}");
            }

            return sb.ToString();
        }

        private static string ToAccessibilityString(Accessibility accessibility) => accessibility switch
        {
            Accessibility.Public => "public",
            Accessibility.Internal => "internal",
            Accessibility.Protected => "protected",
            Accessibility.Private => "private",
            Accessibility.ProtectedOrInternal => "protected internal",
            Accessibility.ProtectedAndInternal => "private protected",
            _ => "internal"
        };

        private static string MakeSafeIdentifier(string fullName)
        {
            var sb = new StringBuilder(fullName.Length + 8);
            foreach (var ch in fullName)
            {
                if (char.IsLetterOrDigit(ch) || ch == '_')
                {
                    sb.Append(ch);
                }
                else
                {
                    sb.Append('_');
                }
            }

            if (sb.Length == 0)
            {
                return "_Reactive";
            }

            if (!char.IsLetter(sb[0]) && sb[0] != '_')
            {
                sb.Insert(0, '_');
            }

            return sb.ToString();
        }
